const languages = ['es', 'ru', 'ua', 'fr', 'de', 'it', 'ca', 'nl', 'da', 'sv', 'no', 'ar', 'zh'];

languages.forEach(lang => {
  const filePath = path.join(messagesDir, `${lang}.json`);
  
  if (!fs.existsSync(filePath)) {
    console.log(`⚠️  File ${filePath} does not exist, skipping...`);
    return;
  }
  
  try {
    const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    
    if (!data.Login) {
      data.Login = {};
    }
    
    // Обновляем все ключи Login
    const langTranslations = translations[lang];
    if (langTranslations) {
      Object.keys(langTranslations).forEach(key => {
        if (typeof langTranslations[key] === 'object' && !Array.isArray(langTranslations[key])) {
          if (!data.Login[key]) {
            data.Login[key] = {};
          }
          Object.assign(data.Login[key], langTranslations[key]);
        } else {
          data.Login[key] = langTranslations[key];
        }
      });
      
      fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
      console.log(`✅ Updated Login translations in ${lang}.json`);
    }
  } catch (error) {
    console.error(`❌ Error processing ${filePath}:`, error.message);
  }
});

console.log('\n✨ Done!');

